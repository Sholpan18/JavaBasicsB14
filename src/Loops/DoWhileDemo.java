package Loops;

public class DoWhileDemo {

	public static void main(String[] args) {
		
		int num=1;
		
		while (num>=3) {
			System.out.println("I am while loop");
			num++;
		}
		System.out.println("_____________________________________");
		
		int num1=1;
		
		do {
			System.out.println("I am do while loop");
			num1++;
		}while (num1<=2);
	}

}
